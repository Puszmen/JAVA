public class MetricConversion {
    double metersToCentimeters (double a) {
        return a * 100;
    }
    double metersToMillimeters (double a) {
        return a * 1000;
    }
    double centimetersToMeters (double a) {
        return a * 0.01;
    }
    double millimetersToMeters (double a) {
        return a * 0.001;
    }
}
