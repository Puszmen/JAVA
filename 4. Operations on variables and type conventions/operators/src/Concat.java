public class Concat {
    public static void main(String[] args) {

        String s1 = "Hello";
        String s2 = "World";
//        concatenation - matching Strings
        System.out.println(s1 + " " + s2);

        int x = 2;
        int y = 3;
        int sum = x + y;
        System.out.println(x + " + " + y + " = " + sum);
        System.out.println("Is X greater than Y ? " + (x > y));
    }
}
