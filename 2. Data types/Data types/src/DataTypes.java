public class DataTypes {
    public static void main(String[] args) {
        // types of data:
        // "byte" (1 byte of memory): -128 to 127
        // "short" (2 bytes of memory): -32 768 to 32 767
        // "int" (4 bytes of memory): -2^31 to 2^31-1
        // "long" (8 bytes of memory): -2^63 to 2^63-1

        // floating point numbers:
        // "float" (4 bytes of memory): 4 numbers after the point
        // "double" (8 bytes of memory): 8 numbers after the point

        // char types (character) - we can take it from Unicode
        // \ - backslash and:
        // \" - quote
        // \' - apostrophe
        // \n - new line
        // \t - tab
        // \\ - backslash

        // logic type: boolean is true or false

        System.out.println("Ala\nma\nkota");
        System.out.print("Liczba int: ");
        System.out.println(25);
        System.out.print("Liczba double: ");
        System.out.println(25.25);
        System.out.print("Znak: ");
        System.out.println('A');
        System.out.print("Am i learning JAVA ? ");
        System.out.println(true);

    }
}
